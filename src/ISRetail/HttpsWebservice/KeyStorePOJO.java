package ISRetail.HttpsWebservice;

/**
 *
 * @author eyeplus
 */
public class KeyStorePOJO {
    private static String keystorename;
    private static String keystorepassword;

    /**
     * @return the keystorename
     */
    public static String getKeystorename() {
        return keystorename;
    }

    /**
     * @param aKeystorename the keystorename to set
     */
    public static void setKeystorename(String aKeystorename) {
        keystorename = aKeystorename;
    }

    /**
     * @return the keystorepassword
     */
    public static String getKeystorepassword() {
        return keystorepassword;
    }

    /**
     * @param aKeystorepassword the keystorepassword to set
     */
    public static void setKeystorepassword(String aKeystorepassword) {
        keystorepassword = aKeystorepassword;
    }

}
